package java8comparesalary;

public class comparesalary {
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getSalary() {
		return salary;
	}
	public void setSalary(double salary) {
		this.salary = salary;
	}
	String id;
	String name;
	double salary;
	public comparesalary(String id, String name, double salary) {
		super();
		this.id = id;
		this.name = name;
		this.salary = salary;
	}
	@Override
	public String toString() {
		return "comparesalary [id=" + id + ", name=" + name + ", salary=" + salary + "]";
	}
	

}
